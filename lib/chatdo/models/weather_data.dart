class WeatherData {
  final double temp;
  final double minTemp;
  final double maxTemp;
  final double pop;
  final double rainAmount;
  final double uvi;
  final String description;

  WeatherData({
    required this.temp,
    required this.minTemp,
    required this.maxTemp,
    required this.pop,
    required this.rainAmount,
    required this.uvi,
    required this.description,
  });
}
